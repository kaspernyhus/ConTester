void generate_line(char *line, uint8_t line_number, uint32_t data) {
  char buffer[10];
  strcpy(line,"\n   |  ");
  sprintf(buffer,"%2d |",line_number);
  strcat(line,buffer);
  for(int i=0; i<24; i++) { // only 24 LSB
    uint32_t temp = 0;
    temp = (data>>i) & 1;
    if(temp==0) strcat(line, " - ");
    else strcat(line," X ");
  }
  strcat(line,"| ");
}


